-- 开源软件版本基线表
CREATE TABLE IF NOT EXISTS `oss_software_baseline` (
    `id` VARCHAR(32) NOT NULL COMMENT '主键ID',
    `sw_id` VARCHAR(32) NOT NULL COMMENT '软件id(关联oss_software_info)',
    `sw_name` VARCHAR(128) NOT NULL COMMENT '软件名称',
    `sw_version` VARCHAR(64) NOT NULL COMMENT '软件版本',
    `sw_category` VARCHAR(12) NOT NULL DEFAULT 'BASE' COMMENT '软件分类: BASE-开源基础软件, TOOL-开源工具软件, CMPNT-开源组件',
    `sw_type` VARCHAR(12) NOT NULL DEFAULT 'MAIN' COMMENT '软件类型: MAIN-主推软件, LIMIT-非主推软件, QUIT-已退出软件',
    `ver_type` VARCHAR(12) NOT NULL DEFAULT 'WHITELIST' COMMENT '版本类型: WHITELIST-白名单, GREYLIST-灰名单, BLACKLIST-黑名单',
    `impl_team_id` VARCHAR(32) NOT NULL COMMENT '引入团队id',
    `impl_team_name` VARCHAR(64) NOT NULL COMMENT '引入团队名称',
    `impl_user_id` VARCHAR(32) NOT NULL COMMENT '引入人id',
    `impl_user_name` VARCHAR(100) NOT NULL COMMENT '引入人姓名',
    `impl_apply_no` VARCHAR(32) DEFAULT NULL COMMENT '引入申请编号',
    `lic_id` VARCHAR(720) DEFAULT NULL COMMENT '许可证id',
    `lic_abbr` VARCHAR(720) DEFAULT NULL COMMENT '许可证简称',
    `last_eval_datetime` DATETIME DEFAULT NULL COMMENT '最后评估时间',
    `last_eval_id` VARCHAR(32) DEFAULT NULL COMMENT '最后评估人id',
    `effect_datetime` DATETIME DEFAULT NULL COMMENT '生效时间',
    `expire_datetime` DATETIME DEFAULT NULL COMMENT '失效时间',
    `sync_datetime` DATETIME DEFAULT NULL COMMENT '同步时间',
    `create_mode` INT(10) NOT NULL DEFAULT 0 COMMENT '创建方式: 0-本系统创建, 1-异地同步',
    `create_user_id` VARCHAR(32) DEFAULT NULL COMMENT '创建者',
    `create_datetime` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `update_user_id` VARCHAR(32) DEFAULT NULL COMMENT '更新者',
    `update_datetime` DATETIME DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    `logic_status` INT(10) NOT NULL DEFAULT 0 COMMENT '逻辑状态: 0-正常, 1-已删除',
    `use_branch_id` VARCHAR(32) DEFAULT NULL COMMENT '使用机构id',
    `use_branch_name` VARCHAR(128) DEFAULT NULL COMMENT '使用机构名称',
    `remark` VARCHAR(200) DEFAULT NULL COMMENT '备注',
    `media_addr` JSON DEFAULT NULL COMMENT '介质地址',
    `is_main_use` VARCHAR(2) NOT NULL DEFAULT '0' COMMENT '是否主推荐使用版本: 0-否, 1-是',
    `applicable_scene` VARCHAR(32) NOT NULL DEFAULT 'ALL_SCENE' COMMENT '适用场景: ALL_SCENE-全场景适用, INNER_SCENE-限定内部使用',
    `applicable_function_range` VARCHAR(32) NOT NULL DEFAULT 'ALL_BANK_USE' COMMENT '适用职能范围: ALL_BANK_USE-全行使用, ONLY_BRANCH_USE-仅分行使用',
    `sw_full_name` VARCHAR(128) DEFAULT NULL COMMENT '开源软件全称',
    `depend_type` VARCHAR(64) DEFAULT NULL COMMENT '组件依赖类型',
    `data_src_flag` VARCHAR(2) DEFAULT NULL COMMENT '数据来源标识: 0-人工引入, 1-组件自动同步',
    `batch_date` VARCHAR(32) DEFAULT NULL COMMENT '批次日期',
    PRIMARY KEY (`id`),
    KEY `idx_sw_id` (`sw_id`),
    KEY `idx_sw_name` (`sw_name`),
    KEY `idx_sw_version` (`sw_version`),
    KEY `idx_sw_category` (`sw_category`),
    KEY `idx_ver_type` (`ver_type`),
    KEY `idx_logic_status` (`logic_status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci COMMENT='开源软件版本基线表';

